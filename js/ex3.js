var num1 = parseInt(prompt("Please enter an integer: "));
var num2 = parseInt(prompt("Please enter another integer: "));

add = num1 + num2
sub = num1 - num2
mult = num1 * num2
div = num1 / num2
mod = num1 % num2

console.log("${num1} + ${num2} = ${add} ");
console.log("${num1} - ${num2} = ${sub} ");
console.log("${num1} * ${num2} = ${mult} ");
console.log("${num1} / ${num2} = ${div} ");
console.log("${num1} % ${num2} = ${mod} ");
